//primitive data type
//string
const hisName : string = "Meskat";  
console.log(hisName);
//number
const num : number = 123;
console.log(num);
//boolean
const isGoing : boolean = true;
console.log(isGoing);
//undefined
const value1 : undefined = undefined;
console.log(value1);
//null
const value2 : null = null;
console.log(value2);

//symbol
const sym : symbol = Symbol();
console.log(sym);

//Non-primitive data type
//array string
const friendsName : string[] = ["Rahim" , "Karim" , "Singha"];
console.log(friendsName);

//array number
const rollNumber : number[] = [1, 23, 11, 23, 45];
console.log(rollNumber);
//tuple - special type of array
const newValues : [string , number , boolean] = ["sd" , 23 , false];
console.log(newValues);





